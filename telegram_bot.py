def screen_stocks() -> tuple[list[dict], str, int]:
    trade_date = datetime.now(KST).strftime("%Y%m%d")
    total      = len(ALL_TICKERS)
    log.info(f"스크리닝 시작 (KST {trade_date}) / {total}개 종목")

    # ── 1단계: 배치 quote로 가격/시총/거래량 필터 ──
    batch_size = CONFIG["batch_size"]
    symbols    = [s for s, _ in ALL_TICKERS]
    mkt_map    = {s: m for s, m in ALL_TICKERS}

    candidates = []  # 1차 필터 통과 종목

    for i in range(0, len(symbols), batch_size):
        batch   = symbols[i:i+batch_size]
        quotes  = yahoo_batch_quote(batch)
        for sym, q in quotes.items():
            try:
                close      = float(q.get("regularMarketPrice", 0) or 0)
                volume     = int(q.get("regularMarketVolume", 0) or 0)
                # 전일 거래량: Yahoo quote에 직접 제공 안 됨
                # → averageDailyVolume10Day (10일 평균) 를 기준으로 상대거래량 계산
                # → 더 나은 신호: 평소 대비 얼마나 터졌는지
                prev_vol = int(q.get("averageDailyVolume10Day", 0) or 0)
                if prev_vol <= 0:
                    prev_vol = int(q.get("averageDailyVolume3Month", 0) or 0)
                market_cap = float(q.get("marketCap", 0) or 0)
                name       = q.get("longName") or q.get("shortName") or sym

                if close < CONFIG["min_price"]:           continue
                if market_cap < CONFIG["min_market_cap"]: continue
                if prev_vol <= 0:                         continue

                vol_ratio = volume / prev_vol * 100
                if vol_ratio < CONFIG["min_vol_ratio"]:   continue

                candidates.append({
                    "symbol":     sym,
                    "name":       name,
                    "market":     mkt_map.get(sym, "KOSPI"),
                    "close":      close,
                    "volume":     volume,
                    "prev_vol":   prev_vol,
                    "vol_ratio":  vol_ratio,
                    "market_cap": market_cap,
                })
            except Exception:
                continue

        if (i // batch_size + 1) % 5 == 0:
            log.info(f"배치 진행: {min(i+batch_size, len(symbols))}/{len(symbols)}")
        time.sleep(0.1)

    log.info(f"1차 필터 통과: {len(candidates)}개")

    # ── 2단계: 이평선 정배열 필터 ──
    results  = []
    ma_s_n   = CONFIG["ma_short"]
    ma_l_n   = CONFIG["ma_long"]

    for c in candidates:
        try:
            chart  = yahoo_chart(c["symbol"], days=40)
            if not chart:
                continue
            closes = get_closes(chart)
            if len(closes) < ma_l_n:
                continue

            ma_s = sum(closes[-ma_s_n:]) / ma_s_n
            ma_l = sum(closes[-ma_l_n:]) / ma_l_n

            if not (c["close"] > ma_s > ma_l):
                continue

            code = c["symbol"].replace(".KS","").replace(".KQ","")
            results.append({
                "ticker":    code,
                "name":      c["name"],
                "market":    c["market"],
                "close":     c["close"],
                "volume":    c["volume"],
                "prev_vol":  c["prev_vol"],
                "vol_ratio": c["vol_ratio"],
                "market_cap":c["market_cap"],
                "ma5":       round(ma_s, 0),
                "ma20":      round(ma_l, 0),
            })
            log.info(f"  ✅ {c['name']}({code}) {c['vol_ratio']:.0f}%")
        except Exception as e:
            log.debug(f"차트 오류 {c['symbol']}: {e}")
        time.sleep(0.08)

    log.info(f"최종 {len(results)}개")
    return sorted(results, key=lambda x: x["vol_ratio"], reverse=True), trade_date, total


