def _parse_codes(s: str, suffix: str, market: str) -> list[tuple]:
    codes = [c.strip() for c in s.split() if c.strip() and len(c.strip()) == 6]
    # 중복 제거
    seen = set()
    result = []
    for c in codes:
        if c not in seen:
            seen.add(c)
            result.append((c + suffix, market))
    return result

ALL_TICKERS: list[tuple[str, str]] = (
    _parse_codes(_KOSPI,  ".KS", "KOSPI") +
    _parse_codes(_KOSDAQ, ".KQ", "KOSDAQ")
)

