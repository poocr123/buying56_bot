def start_health_server():
    port = int(os.getenv("PORT", "8080"))
    class Handler(BaseHTTPRequestHandler):
        def do_GET(self): self.send_response(200); self.end_headers(); self.wfile.write(b"OK")
        def log_message(self, *a): pass
    threading.Thread(target=HTTPServer(("0.0.0.0", port), Handler).serve_forever, daemon=True).start()
    log.info(f"헬스체크 서버: 포트 {port}")

def main():
    if not TELEGRAM_TOKEN:   print("❌ TELEGRAM_TOKEN 없음"); sys.exit(1)
    if not TELEGRAM_CHAT_ID: print("❌ TELEGRAM_CHAT_ID 없음"); sys.exit(1)
    async def run():
        start_health_server()
        app = Application.builder().token(TELEGRAM_TOKEN).build()
        app.add_handler(CommandHandler("start",  cmd_start))
        app.add_handler(CommandHandler("scan",   cmd_scan))
        app.add_handler(CommandHandler("test",   cmd_test))
        app.add_handler(CommandHandler("status", cmd_status))
        app.job_queue.run_daily(scheduled_scan,
            time=datetime.strptime("16:10", "%H:%M").time(), name="daily_scan")
        log.info("봇 시작 (폴링)")
        await app.initialize()
        await app.start()
        await app.updater.start_polling(allowed_updates=Update.ALL_TYPES, drop_pending_updates=True)
        log.info("✅ 폴링 시작")
        import signal
        stop_event = asyncio.Event()
        loop = asyncio.get_event_loop()
        for sig in (signal.SIGINT, signal.SIGTERM): loop.add_signal_handler(sig, stop_event.set)
        await stop_event.wait()
        await app.updater.stop(); await app.stop(); await app.shutdown()
    asyncio.run(run())

if __name__ == "__main__":
    main()
