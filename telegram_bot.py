def yahoo_batch_quote(symbols: list[str]) -> dict[str, dict]:
    """v7 배치 quote API — 가격/시총/거래량 한번에"""
    url = "https://query1.finance.yahoo.com/v7/finance/quote"
    params = {"symbols": ",".join(symbols)}
    try:
        r = requests.get(url, headers=HEADERS, params=params, timeout=20)
        r.raise_for_status()
        quotes = r.json().get("quoteResponse", {}).get("result", [])
        return {q["symbol"]: q for q in quotes if "symbol" in q}
    except Exception as e:
        log.warning(f"배치 quote 실패: {e}")
        return {}


def yahoo_chart(symbol: str, days: int = 40) -> dict | None:
    """v8 chart — 일봉 종가 시리즈"""
    url = f"https://query1.finance.yahoo.com/v8/finance/chart/{symbol}?interval=1d&range={days}d"
    try:
        r = requests.get(url, headers=HEADERS, timeout=15)
        if r.status_code != 200:
            return None
        result = r.json().get("chart", {}).get("result")
        return result[0] if result else None
    except Exception:
        return None


def get_closes(chart: dict) -> list[float]:
    closes = chart.get("indicators", {}).get("quote", [{}])[0].get("close", [])
    return [c for c in closes if c is not None]


